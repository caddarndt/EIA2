 function main(): void {
        let cards: number;
        let p: string = prompt("Mit wie vielen Karten willst du spielen?");
        cards = Number(p);
        if (cards > 20) { cards = 20; } 

        for (let i: number = 0; i < cards; i++) {

            let randomCard: number = random(deck.length);
            placeDiv(deck[randomCard].color, deck[randomCard].value, i); 
            deck.splice(randomCard, 1); 
            continue;
        }
    }
    function random(_math: number): number {
        return Math.floor(Math.random() * Math.floor(_math));
    }
    function placeDiv(_color: string, _x: string, _n: number): void {
        let div: HTMLDivElement = document.createElement("div");
        document.body.appendChild(div);
        div.setAttribute("id", "a" + _n);
        document.getElementById("a" + _n).innerHTML += _x;
        let s: CSSStyleDeclaration = div.style;
        s.backgroundColor = _color;
        s.border = "thin solid black";
        s.textAlign = "center";
        s.position = "absolute";
        s.width = 90 + "px";
        s.height = 140 + "px";
        s.right = 50 + "px";
        s.top = 20 + "px";
        s.borderRadius = 5 + "px";
        s.color = "white";
        s.left = (_n + 1) * 100 + "px";
        s.right = (_n + 1) * 100 + "px";
    }    
    
   
    document.addEventListener("DOMContentLoaded", main);
    
